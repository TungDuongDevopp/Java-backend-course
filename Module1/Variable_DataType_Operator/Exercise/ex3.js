// Bài 3
// Nhập số a
let a = parseFloat(prompt("Vui lòng nhập số a: "));
// Nhập số b
let b = parseFloat(prompt("Vui lòng nhập số b: "));

// Kiểm tra a có bằng b không (==)
console.log("a có bằng b không (==):", a == b);

// Kiểm tra a có lớn hơn b không (>)
console.log("a có lớn hơn b không (>):", a > b);

// Kiểm tra a có khác b không (!=)
console.log("a có khác b không (!=):", a != b);