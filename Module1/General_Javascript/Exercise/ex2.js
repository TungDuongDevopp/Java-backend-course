//Thực hiện gán a
let a = 10;

//Thực hiện gán b
let b = 5;

//Thực hiện tính tổng
let sum = a+b;

//Hiển thị bằng console.log
console.log(`Tổng của hai số ${a} và ${b} là: ${sum}`);

//Hiển thị bằng document.writeln
document.writeln(`Tổng của hai số ${a} và ${b} là: ${sum}`);

//Hiển thị bằng alert
alert(`Tổng của hai số ${a} và ${b} là: ${sum}`);